def get_translation(key: str, lang: str, **kwargs) -> str:
    """
    Get translation for a key in specified language.
    Falls back to English if translation not found.
    
    Args:
        key: Translation key
        lang: Language code (en, ar, fa, ru)
        **kwargs: Format arguments for string interpolation
    
    Returns:
        Translated string
    """
    lang = lang.upper() if lang else "EN"
    
    if key not in REALTY_TRANSLATIONS:
        return f"[Missing: {key}]"
    
    translations = REALTY_TRANSLATIONS[key]
    
    # Get translation or fallback to English
    text = translations.get(lang, translations.get("EN", f"[Missing: {key}]"))
    
    # Handle list values (like budget options)
    if isinstance(text, list):
        return text
    
    # Apply format arguments if any
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass  # Ignore missing format args
    
    return text


def get_button_text(button_key: str, lang: str) -> str:
    """Get button text for specified language"""
    return get_translation(button_key, lang)
